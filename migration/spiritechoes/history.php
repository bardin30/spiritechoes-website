<td width="70%" valign="top"> <p><h1>Thank 
you for your interest in Spirit Echoes. Although some may think the name is Native 
American, it simply refers to the ability to tap into the Creator within through 
artistic expression and echo that Spirit to others.</p>
<p>Spirit Echoes started out 
as a newsletter in Austin, Texas and was founded in October 1991 by Melodie Spears. 
The newsletter, established to promote emerging artists, quickly evolved into 
an art gallery in the luxurious lobby of the downtown Omni Hotel. The purpose 
of the gallery was to enable emerging artists to display and sell their work to 
the public, to market the artwork through newsletters and exhibitions, and to 
encourage the creative spirit in everyone. A second location on Far West Blvd 
was opened in October 1999 and closed in December 2003 to enable Melodie to pursue 
her own creativity and to establish another venue in which to support the arts 
in Austin.</p>
<p>Through the years, Ms. Spears 
acquired a large following of artists and collectors alike&#8230;some 8,000 kindred 
spirits joined by their love for art and creativity. The Spirit Echoes Annual 
Fine Art Festival invites these and thousands more to experience creative expression 
at it&#8217;s best.</p>
