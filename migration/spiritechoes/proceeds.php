VSA Arts of Texas (see <a href="http://www.vsatx.org/" target="_parent">www.vsatx.org</a>) is a 
non-profit 501 (c) corporation that promotes the creative power of people with 
disabilities. Based in Austin, the organization has been in existence for over 
eight years, supporting creativity through workshops, festivals, conferences and 
exhibitions. They bring the visual, performing and literary arts to adults and 
children through a wide array of programs and services. All programs are in compliance 
with the American With Disabilities Act (ADA) and with the Texas Essential Knowledge 
and Skills (TEKS). <br>
<br>
Spirit Echoes Fine Art Festival is partnering with VSA Arts of Texas to create 
more public awareness for this important organization and to designate VSA Arts 
as this year's "proceeds recipient". VSA will receive a portion of the proceeds 
in addition to all of the donations received at the "Kid's Krafts" area in the 
festival. For a small donation, children at the festival can visit the Kids Krafts 
area and create a piece of art to take home. Activities will include painting 
with yarn, mono printmaking, mask and puppet making, reposee' , making fans and 
other items.<br>
<br>
Be sure to visit the VSA Arts of Texas booth at the festival to view some of their 
most talented artists' work. <br>
