<font face="Arial, Helvetica, sans-serif">
<br>
<br>
I knew a wonderful man in the Texas Hill Country that I adopted as my grandfather... AM 
Gribble - I called him Mr. Gribble. He was a poet and a teacher, a philosopher 
of sorts, and he loved the jackrabbit cactus found in the rolling hillside. Maybe 
they were symbolic to him of the survivor instinct in all of us... to develop a 
prickly exterior as a shell, shielding us from life's pain of loss. I can't look 
at a cactus without thinking of Mr. Gribble and other mentors that came my way 
in this lifetime, then passed out of it so quietly. </font> </td>
