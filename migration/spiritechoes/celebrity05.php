    <td width="60%" bgcolor="#E3E3FF"> <h1 class="bodyText">It is rare when one 
        achieves such success in his or her lifetime that the mere mention the 
        first or last name brings the face and talent to one's mind. Such is the 
        case with Amado Pena, contemporary southwestern artist of our time. Like 
        Willie, Merle, Picasso, and "the Donald"... it only takes half of the 
        name to recognize the subject of the conversation, and Amado is a name 
        mentioned in art circles regularly. One can hardly walk through an airport 
        without seeing one of his images on a tee shirt or souvenir. But it wasn't 
        always like that.<br>
        <br>
        Amado M Pena Jr. was born and educated in Texas. A mestizo of Mexican 
        and Yaqui ancestry, Pena celebrates the strength of a people who meet 
        the harsh realities of life in an uncompromising land and his work is 
        a tribute to these Native Americans who survive by living in harmony with 
        the adversarial, untamed environment. His work includes acrylic paintings 
        on paper, board and canvas; mixed media works in pastel and prisma color 
        and original monotypes, etchings, serigraphs and lithographs. His art 
        is a superb blending of the landscapes and people of the Southwest - his 
        friends and family in a myriad of contrasting colors both brilliant and 
        subtle.<br>
        <br>
        Pena taught at L.C. Anderson High School from 1974 - 80 and a decade later 
        was an instructor and artist in residence at the Austin Community College. 
        When not traveling to gallery shows, he divides his time between studios 
        in Austin and Santa Fe. He climbed the shaky ladder of success in the 
        art world slowly, but steadily and even dropped a few rungs from time 
        to time, but has always managed to withstand the pitfalls of the industry 
        and keep his high ranking in the eyes of his collectors nationwide. Although 
        Pena's admirers come from different cultures and traditions, from all 
        ages and parts of the world, the strength of his vision crosses such boundaries 
        and speaks to them of hope, endurance, and the unconquerable dignity of 
        man. These are the qualities that are etched into the faces of Amado Pena's 
        art, and indeed etched into his own countenance as well.<br>
        <br>
        "I feel blessed having seen and touched the beautiful things that speak 
        so proudly of who we are. Our gifts to the world are our history, our 
        art," says Pena. "My inspiration has always been artists. It is they who 
        saw the world around them and sought to paint it. It is the artisans who 
        touched the earth and molded it into objects of beauty... they are the 
        true historians of a people's culture and vision."<br>
