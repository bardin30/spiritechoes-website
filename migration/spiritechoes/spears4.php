<strong>&quot;The Light Beyond the Gate&quot;</strong> </font> <font face="Arial, Helvetica, sans-serif"><br>
<br>
As I created this acrylic painting, the colors and shapes evolved into 
a layered maze, reminding me of our lives and the many layers we have 
to peel back to find our true inner being.  The gate appeared quite 
magically, as did the spirit shapes beyond the gate... then came the portals 
of light coming through the layers and I understood what this painting 
was all about.  I lost my father on the first day of January 1999, but 
I feel his presence constantly.  I knew he was with me the day this 
piece was created.  I believe that souls of our loved ones stay with 
us throughout our lives... communicating in many ways... some of which are 
very obvious, some very subtle.  It is our task to stay open to that 
communication... to shut off the mind and listen with the heart.  I hope 
you have many hours of enjoyment and a sense of comfort from this acrylic painting.
