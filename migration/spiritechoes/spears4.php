<strong>&quot;One Light... Three Visions&quot;</strong>
</font>
<font face="Arial, Helvetica, sans-serif">
<br>
<br>
After a twenty-five year sabbatical from painting, I picked up my brushes 
and began creating this, my first of many paintings to come.  As I painted, 
I was thinking about how many doors we have to open in order to find our 
one true light - our one truth - our purpose on the planet... that one thing 
that we alone are meant to do.  When we do find our light, it is perceived 
by others in many different ways, thus the three fold reflections of the 
light in the painting.  This acrylic painting, like all my others to follow, 
was not "planned" per se, but evolved out of a desire to capture on canvas 
the joy of painting with bright, undiluted color... the joy of rekindling my 
intense desire to create.  I hope you enjoy this acrylic painting as much 
as I enjoyed creating it.  You will find a thumbprint or fingerprint 
somewhere in the piece symbolizing my last and final "touch.     
