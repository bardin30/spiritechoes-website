<td  width="50%" bgcolor="#E3E3FF"><font size="+1" face="Arial, Helvetica, sans-serif"> 
<strong>&quot;Light Ascending&#8230;Souls Rising&quot;</strong></font>
<font face="Arial, Helvetica, sans-serif"><br>
<br>
This acrylic painting evolved out of my desire to create a piece symbolic of 
New York City and the 9/11 tragedy... the many souls who were lost and the many 
prayers for the victims that went up that day from nations around the world.  
The buildings seemed better suited as cylinders, or vessels that held the souls 
of those who were affected the most that terrible day.  The water symbolizes the 
tears of so many, watching our televisions in horror as the towers disintegrated 
before our eyes.  As this painting evolved, the water became the sky and the 
vessels began to "float" above it all in a mystical way.  The spirit figures 
became dove-like as they ascended into the clouds... as if to say "peace."  The 
bright colors brought a paradoxical sense of joyfulness to the piece while 
the reflections in the water, a sense of serenity.  I place a fingerprint or 
a thumbprint somewhere in all my paintings when I have finished the final 
touches... but in this particular piece, each light in every tower is a fingerprint.  
I hope this unique painting brings you peace and serenity in the unpredictable world. 
