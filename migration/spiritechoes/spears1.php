<td width="50%" rowspan="2" bgcolor="#E3E3FF"> <font size="+1" face="Arial, Helvetica, sans-serif"><strong>&quot;Fall 
of the House of Cards&quot; </strong></font> <font face="Arial, Helvetica, sans-serif"><br>
<br>
When I created this piece, it was during a time of real conflict for organized 
religion. I wanted to portray the ongoing struggle between the spiritual beliefs 
of mankind... and yes, even man's struggle within himself to find his own spiritual 
path. Although unintentional on my part, the circular shape became a mandala, 
which then shadowed an eclipse, or void which may represent the moment when darkness 
and light, Good and Evil are separated in the world... marking the beginning of 
duality. The white flower in the mandala represents purity, and the calla lily 
specifically being womb-like, may signify a divine child or the Christ. Other 
religious symbols surfaced as I worked on the piece and of course, the playing 
cards, being unstable, fall as they may in a house of cards. I was reminded to 
go within to seek the truth that is my own and that we all have the answers we 
need if we just listen to our heart. </font> </td>
