<td width="50%" bgcolor="#FFFFFF"> <font size="+1" face="Arial, Helvetica, sans-serif"><strong>&quot;Journey 
to the Center&quot; </strong></font> <font face="Arial, Helvetica, sans-serif"><br>
<br>
As I created this acrylic painting, it changed many times and continued to 
evolve until the very last brush stroke.  The thought in my mind as I painted 
it, was to portray the center of the universe, but it evolved into regions of 
the earth, primarily the Southwest and Colorado... places where I feel very "centered" 
and close to Spirit.  The dripping paint represents rain from the storm clouds above, 
which create the canyons and the mountains below... the White Sands evolve into aspen 
groves, the forests and then back into the icy mountain ranges.  The Hopi Native 
Americans tell a story about the emergence or spiral symbol.  The story goes that 
this is the third world to evolve from a spiral in space and that when people came 
out of the spiral, the last things out were our finger tips and toes, thus the 
spirals on our appendages to remind us where we came from.  The stairs are 
symbolic of our climb to find our "oneness" with our creator; the yellow is 
our light shining from deep within.  I hope your journey to find your center is a 
gentle one and that you receive much joy and contemplation from "Journey to the Center."
